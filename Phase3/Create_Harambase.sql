DROP TABLE  HARAMBASE_ADMIN CASCADE CONSTRAINTS;
CREATE TABLE HARAMBASE_ADMIN(
	USERID INTEGER PRIMARY KEY,
	UNAME VARCHAR(10) UNIQUE,
	EMAIL VARCHAR(20),
	FNAME VARCHAR(15), 
	LNAME VARCHAR(15),
	PASSWORD VARCHAR(16),
  CREATORID Integer,
  FOREIGN KEY (CREATORID) REFERENCES HARAMBASE_ADMIN(USERID) 
    ON DELETE SET NULL);
  
DROP TABLE  HARAMBASE_MEMBER CASCADE CONSTRAINTS;
CREATE TABLE HARAMBASE_MEMBER(
	USERID INTEGER PRIMARY KEY,
	UNAME VARCHAR(10) UNIQUE,
	EMAIL VARCHAR(20),
	FNAME VARCHAR(15), 
	LNAME VARCHAR(15),
	PASSWORD VARCHAR(16),
  CREATORID INTEGER,
  ISBUYER NUMBER(1, 0),
  ISELLER NUMBER(1, 0),
  FOREIGN KEY (CREATORID) REFERENCES HARAMBASE_ADMIN(USERID) 
    ON DELETE SET NULL);

DROP TABLE  HARAMBASE_ITEM CASCADE CONSTRAINTS;
CREATE TABLE HARAMBASE_ITEM(
	ITEMID INTEGER PRIMARY KEY,
	ITEMNAME VARCHAR(150),
	ITEMCATEGORY VARCHAR(50),
  STARTPRICE NUMBER(6,2),
  DESCRIPTION VARCHAR(300),
  SELLERID INTEGER,
  AUCTIONSTARTTIME TIMESTAMP(3),
  AUCTIONENDTIME TIMESTAMP(3),
  FOREIGN KEY (SELLERID) REFERENCES HARAMBASE_MEMBER(USERID) 
    ON DELETE SET NULL

);
    
DROP TABLE  HARAMBASE_FEEDBACK CASCADE CONSTRAINTS;
CREATE TABLE HARAMBASE_FEEDBACK(
	ITEMID INTEGER PRIMARY KEY,
	OVERALLRATING NUMBER(3,1),
	ITEMQUALITY NUMBER(2,1),
	DELIVERY NUMBER(2,1),
  COMMENTS VARCHAR(300),
  FOREIGN KEY (ITEMID) REFERENCES HARAMBASE_ITEM(ITEMID) 
    ON DELETE SET NULL
);

DROP TABLE  HARAMBASE_BID CASCADE CONSTRAINTS;
CREATE TABLE HARAMBASE_BID(
	USERID INTEGER, 
  ITEMID INTEGER,
	BIDDINGTIME TIMESTAMP(3),
  MAXBIDLIMIT NUMBER(6,2),
  PRIMARY KEY (USERID, ITEMID),--THIS IS HOW YOU DEFINE PRIMARY KEY
  FOREIGN KEY (USERID) REFERENCES HARAMBASE_MEMBER(USERID) 
    ON DELETE SET NULL,
  FOREIGN KEY (ITEMID) REFERENCES HARAMBASE_ITEM(ITEMID) 
    ON DELETE SET NULL
);

